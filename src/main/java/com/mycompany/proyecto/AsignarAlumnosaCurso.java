        // TODO add your handling code here:
        Curso cursoSeleccionado = null;
        Alumnos alumnoSeleccionado = null;

        // Buscar el curso seleccionado
        for (Curso e : Proyecto.cursos) {
            if (e.nombre.equals(jComboBox1.getSelectedItem().toString())) {
                cursoSeleccionado = e;
                break;
            }
        }

        // Buscar el alumno seleccionado
        for (Alumnos e : Proyecto.alumnos) {
            if (e.nombre.equals(jComboBox2.getSelectedItem().toString())) {
                alumnoSeleccionado = e;
                break;
            }
        }

        if (cursoSeleccionado != null && alumnoSeleccionado != null) {
            // Verificar si el alumno ya est치 asignado a este curso
            if (alumnoSeleccionado.cursos.contains(cursoSeleccionado)) {
                JOptionPane.showMessageDialog(this, "El alumno ya est치 asignado a este curso.");
                return;
            }

            // Verificar si el curso ya tiene 10 estudiantes
            if (cursoSeleccionado.alumnos.size() >= 10) {
                JOptionPane.showMessageDialog(this, "Este curso ya tiene 10 estudiantes asignados.");
                return;
            }

            // Verificar si el alumno ya est치 asignado a 5 cursos
            if (alumnoSeleccionado.cursos.size() >= 5) {
                JOptionPane.showMessageDialog(this, "El alumno no puede estar asignado a m치s de 5 cursos.");
                return;
            }

            // Asignar el curso al alumno y el alumno al curso
            cursoSeleccionado.alumnos.add(alumnoSeleccionado);
            alumnoSeleccionado.cursos.add(cursoSeleccionado);
            JOptionPane.showMessageDialog(this, "Curso asignado exitosamente.");
        }



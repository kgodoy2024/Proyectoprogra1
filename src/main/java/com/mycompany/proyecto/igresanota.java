        ModificarNota.Nota = nota;
        jTextField1.setText("");
        JOptionPane.showMessageDialog(this, "Nota ingresada correctamente");

        for (Alumnos alumno : Proyecto.alumnos){
            if(alumno.carne.equals(ModificarNota.carne)){
                for (Curso cu : alumno.cursos){
                    if(cu.nombre.equals(this.nombrecurso)){
                        
                        cu.nota = nota;
                        break;
                    }
                }
               
            }
        }


private void actualizarTabla(){

   
            profesor EmpresaSeleccionada=null;
            EmpresaSeleccionada = Proyecto.profesorLogeado;

        
        if(EmpresaSeleccionada!=null){
      
        DefaultTableModel data = new DefaultTableModel(new String[]{"ID","Nombre","Secci√≥n","F.Inicio","F.Fin","H.Inicio","H.Fin","Profesor"},EmpresaSeleccionada.cursos.size());
        jTable1.setModel(data);
        
        int row=0;
        
        for(Curso cu : EmpresaSeleccionada.cursos){
            jTable1.setValueAt(cu.id, row, 0);
            jTable1.setValueAt(cu.nombre, row, 1);  
            jTable1.setValueAt(cu.seccion, row, 2);
            jTable1.setValueAt(cu.fechainicio, row, 3);
            jTable1.setValueAt(cu.fechafin, row, 4);  
            jTable1.setValueAt(cu.horainicio, row, 5);
            jTable1.setValueAt(cu.horafin, row, 6);
                        String nombreEmpresa = "";
            for(profesor p :Proyecto.profesores){
                if(p.cursos.contains(cu)){
                    nombreEmpresa = p.nombre;
                    break;
                }
            }
            jTable1.setValueAt(nombreEmpresa, row, 7);
            row++;
            }
        }
    }


public void leer(String rutaConNombre) {
        JSONParser jp = new JSONParser();
        try {
            FileReader r = new FileReader(rutaConNombre);
            Object obj = jp.parse(r);
            JSONArray cursosList = (JSONArray) obj;

            DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();

            for (Object c : cursosList) {
                JSONObject cursoObj = (JSONObject) c;
                long id = (long) cursoObj.get("id");
                String nombre = (String) cursoObj.get("nombre");
                String seccion = (String) cursoObj.get("seccion");
                String fechaInicio = (String) cursoObj.get("fecha_inicio");
                String fechaFin = (String) cursoObj.get("fecha_fin");
                String horaInicio = (String) cursoObj.get("hora_inicio");
                String horaFin = (String) cursoObj.get("hora_fin");
                String profesor = (String) cursoObj.get("profesor");

                // Crear objeto Curso y agregarlo a la lista
                Curso curso = new Curso();
                curso.id = (int) id;
                curso.nombre = nombre;
                curso.seccion = seccion;
                curso.fechainicio = fechaInicio;
                curso.fechafin = fechaFin;
                curso.horainicio = horaInicio;
                curso.horafin = horaFin;
                curso.profe = profesor;

                Proyecto.cursos.add(curso);

                // Agregar fila a la tabla
                tableModel.addRow(new Object[]{id, nombre, seccion, fechaInicio, fechaFin, horaInicio, horaFin, profesor});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

     



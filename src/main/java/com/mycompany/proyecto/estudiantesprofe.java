    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Curso empresaSeleccinada = null;

        for(Curso e : Proyecto.cursos){
            if(e.nombre.equals(jComboBox1.getSelectedItem().toString())){
                empresaSeleccinada = e;
                break;
            }
            
        }

        if(empresaSeleccinada!=null){

            DefaultTableModel data = new DefaultTableModel(new String[]{"Carn√©","Nombre","Nota"},empresaSeleccinada.alumnos.size());
            jTable1.setModel(data);

            int row=0;

            for(Alumnos e : empresaSeleccinada.alumnos){
                jTable1.setValueAt(e.carne, row, 0);
                jTable1.setValueAt(e.nombre, row, 1);
                jTable1.setValueAt(e.Nota, row, 2);
                row++;
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

